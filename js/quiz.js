  const root = document.getElementById('quiz-root');
  const scoreEl = document.getElementById('quiz-score');
  if (!root) return;

  let answered = 0;
  let correct = 0;

  function updateScore() {
    scoreEl.textContent = 'ניקוד: ' + correct + ' / ' + answered + ' (מתוך ' + QUESTIONS.length + ' שאלות)';
  }

  QUESTIONS.forEach(function (item, qi) {
    const box = document.createElement('article');
    box.className = 'quiz-q reveal';

    const h = document.createElement('h3');
    h.textContent = (qi + 1) + '. ' + item.q;
    box.appendChild(h);

    const opts = document.createElement('div');
    opts.className = 'quiz-options';

    const fb = document.createElement('div');
    fb.className = 'quiz-feedback';
    fb.setAttribute('role', 'status');

    item.options.forEach(function (text, oi) {
      const btn = document.createElement('button');
      btn.type = 'button';
      btn.className = 'quiz-option';
      btn.textContent = text;
      btn.addEventListener('click', function () {
        if (box.dataset.done) return;
        box.dataset.done = '1';
        answered++;
        const ok = oi === item.answer;
        if (ok) correct++;
        opts.querySelectorAll('.quiz-option').forEach(function (b, bi) {
          b.disabled = true;
          if (bi === item.answer) b.classList.add('correct');
        });
        if (!ok) btn.classList.add('wrong');
        fb.textContent = (ok ? 'נכון. ' : 'לא מדויק. ') + item.fb;
        fb.classList.add('show', ok ? 'ok' : 'err');
        updateScore();
      });
      opts.appendChild(btn);
    });

    box.appendChild(opts);
    box.appendChild(fb);
    root.appendChild(box);
  });

  updateScore();

  /* חשיפת השאלות שנוספו דינמית */
  if (window.IntersectionObserver && !window.matchMedia('(prefers-reduced-motion: reduce)').matches) {
    const io = new IntersectionObserver(function (entries) {
      entries.forEach(function (e) {
        if (e.isIntersecting) { e.target.classList.add('visible'); io.unobserve(e.target); }
      });
    }, { threshold: 0.1 });
    root.querySelectorAll('.reveal').forEach(function (el) { io.observe(el); });
  } else {
    root.querySelectorAll('.reveal').forEach(function (el) { el.classList.add('visible'); });
  }
